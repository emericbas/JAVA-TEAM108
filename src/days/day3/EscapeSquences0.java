package days.day3;

public class EscapeSquences0 {
    public static void main(String[] args) {
        System.out.println("Spain\nItaly\nEnglandNorway");
        // \n insert new line into the text
        System.out.println("Spain\tEngland\tNorway\tVan");
        //  \t put a tab into the text after at this point
        System.out.println("spain \bankara");
        // \b a backspace
        System.out.println(" Ali said Taha:\"Come here\"");
        System.out.println("Ali said Taha:\\Come here\\");
        System.out.println("Ali said Taha:'Come here'");
        System.out.println("Ali said Taha:\"Come here\"");
        System.out.println("hello canim");
        System.out.println("\\\\hello canim\\\\");
    }
}
